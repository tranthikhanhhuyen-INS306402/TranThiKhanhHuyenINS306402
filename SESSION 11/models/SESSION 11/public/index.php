<?php

require_once __DIR__ . '/../controllers/EntityController.php';

if (!class_exists('EntityController')) {
    die("Controller not loaded!");
}

$controller = new EntityController();

$action = $_GET['action'] ?? 'index';

switch ($action) {
    case 'create':
        $controller->create();
        break;
    case 'store':
        $controller->store();
        break;
    case 'edit':
        $controller->edit();
        break;
    case 'update':
        $controller->update();
        break;
    case 'delete':
        $controller->delete();
        break;
    default:
        $controller->index();
}